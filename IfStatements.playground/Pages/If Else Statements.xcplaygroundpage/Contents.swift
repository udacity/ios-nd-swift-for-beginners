//: ## If-Else Statements

//:    if <condition> {
//:        <code to execute>
//:    } else {
//:        <code to execute>
//:    }

var breakfast = true
var lunch = false
var dinner = false

if breakfast {
    print("Good morning!")
} else  if lunch {
    print("Good afternoon!")
} else if dinner {
    print("Good evening!")
} else {
    print("Hello!")
}

